import re
import json

def parse_receipt(text):
    def clean_val(val):
        # Удаляем пробелы и спецсимволы, меняем запятую на точку для расчетов
        return float(val.replace('\u00a0', '').replace(' ', '').replace(',', '.'))

    data = {
        "items": [],
        "total_amount": 0.0,
        "date_time": None,
        "payment_method": "Unknown"
    }

    # Поиск даты и времени
    date_match = re.search(r'(\d{2}\.\d{2}\.\d{4}\s\d{2}:\d{2}:\d{2})', text)
    if date_match:
        data["date_time"] = date_match.group(1)

    # Поиск метода оплаты
    if "Банковская карта" in text:
        data["payment_method"] = "Card"

    # Поиск итоговой суммы
    total_match = re.search(r'ИТОГО:\s*([\d\s,]+)', text)
    if total_match:
        data["total_amount"] = clean_val(total_match.group(1))

    # Поиск товаров (номер, название, цена)
    item_pattern = re.compile(r'(\d+)\.\n(.*?)\n[\d\s,]+ x [\d\s,]+\n([\d\s,]+)\nСтоимость', re.DOTALL)
    
    for match in item_pattern.finditer(text):
        data["items"].append({
            "id": int(match.group(1)),
            "name": match.group(2).replace('\n', ' ').strip(),
            "price": clean_val(match.group(3))
        })

    return data

